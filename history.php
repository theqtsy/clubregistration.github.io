<h2>School History</h2>
<p> 
Sekolah Menengah Kebangsaan Dato 'Sheikh Ahmad was established on 1 January 1964 as the first National Stream Secondary School in Perlis. Prior to this, he was placed at Derma English School, Kangar from May 1958.
The school was inaugurated on 12 June 1964 by Y.T.M Tengku Suleiman Ibni Almarhum Tengku Bendahara Abu Bakar as the Arau Malay Secondary School. On 3 November 1966, the school was given a new name, namely Sekolah Menengah Kebangsaan Dato 'Sheikh Ahmad by accepting only Form 4 and 5 students, while Forms 1, 2, and 3 studied at Sekolah Melayu Arau.</p>

